/**
 * A control that displays some text that can potentially be edited.

 * ## Usage
 * ### Simple
 * <code><|{value}|input|></code>
 * ### Advanced
 * <code><|{value}|input|></code>
 * <br>or with properties<br>
 * <code><|{value}|input|properties={properties}|></code>
 * @element input
 */
class input extends propagate {

     /**
     * bound to a value
     * @type {dynamic(any), default property}
     */
     value;

}
